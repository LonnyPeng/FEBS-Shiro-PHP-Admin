namespace app\admin\controller;

use app\lonny\controller\Plugin\QRcode AS LQRcode;

class QRcode extends Common 
{
    public function index()
    {
        $errorCorrectionLevel = "L"; // 纠错级别：L、M、Q、H  
        $matrixPointSize = "8"; // 点的大小：1到10

        $q = $this->param('q');

        LQRcode::png($q, false, $errorCorrectionLevel, $matrixPointSize);
        exit();
    }
